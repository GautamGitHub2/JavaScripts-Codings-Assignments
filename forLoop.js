// For Loop

/*

for(initialization;condition;increment/decrement)
{
    statements;
}

*/

console.log("for loop Example: 5");

let i=10;

for(i=1;i<=10;i++)
{
    console.log(i);
}

console.log("for loop Example: 6");

let j=10;

for(j=10; j>0; j--)
{
    console.log(j);
}

console.log("for loop Example: 7");

for(let k=2; k<=20; k+=2)
{
    console.log(k);
}

// "For in loop" and "for of loop" are designed for JS objects and JS Arrays